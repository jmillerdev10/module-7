class Book:
    def __init__(self, title, author, publisher, published_date, pages, non_fiction, genre):
        self.title = title
        self.author = author
        self.publisher = publisher
        self.published_date = published_date
        self.pages = pages
        self.non_fiction = non_fiction
        self.genre = genre

    def open():
        "open the book"
    
    def turnPage():
        "turn to the next page"

